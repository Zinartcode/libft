#include "libft.h"

void	ft_remove_digits(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
			write(1, &str[i], 1);
		i++;
	}
	write(1, "\n", 1);
}
