#include "libft.h"

void	ft_new_line(char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == ' ')
		{
			write(1, "\n", 1);
			i++;
		}
		write(1, &str[i], 1);
		i++;
	}
	write(1, "\n", 1);
}
